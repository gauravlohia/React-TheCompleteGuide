// let and const are introduced in ES6 to create variables
// let are used to create scoped variables
// const are used to create variables which cannot be changed once assigned

let myName = "Gaurav";
console.log(myName); // Gaurav

myName = "Gappu";
console.log(myName); // Gappu

const lastName = "Lohia";
console.log(lastName); // Lohia

lastName = "LOHIA"; // error cannot reassign a const